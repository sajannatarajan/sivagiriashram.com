function escapeHtml(value) {
  return value
    .replaceAll("&", "&amp;")
    .replaceAll("<", "&lt;")
    .replaceAll(">", "&gt;")
    .replaceAll('"', "&quot;")
    .replaceAll("'", "&#39;");
}

function getCategories() {
  return ["All", ...new Set(library.map((item) => item.category))];
}

function getFilteredLibrary() {
  const query = state.query.trim().toLowerCase();

  return library.filter((item) => {
    const matchesCategory = state.category === "All" || item.category === state.category;
    const haystack = [
      item.title,
      item.category,
      item.sourceLanguage,
      item.summary,
      item.commentary,
      ...Object.values(item.texts)
    ].join(" ").toLowerCase();

    return matchesCategory && (!query || haystack.includes(query));
  });
}

function ensureSelectedVisible(items) {
  if (!items.some((item) => item.id === state.selectedId)) {
    state.selectedId = items[0]?.id || null;
  }
}

function renderFilters() {
  categoryFilters.innerHTML = getCategories()
    .map((category) => {
      const activeClass = category === state.category ? "is-active" : "";
      return `<button class="button filter-button ${activeClass}" type="button" data-category="${escapeHtml(category)}">${escapeHtml(category)}</button>`;
    })
    .join("");
}

function renderLibrary() {
  const items = getFilteredLibrary();
  ensureSelectedVisible(items);

  if (!items.length) {
    libraryGrid.innerHTML = '<div class="empty-state">No works match this search yet. Try a broader word such as prayer, equality, or Advaita.</div>';
    renderReader();
    return;
  }

  libraryGrid.innerHTML = items
    .map((item) => {
      const selectedClass = item.id === state.selectedId ? "is-selected" : "";
      return `
        <article class="library-card ${selectedClass}" data-id="${escapeHtml(item.id)}">
          <div class="card-meta">
            <span class="pill">${escapeHtml(item.category)}</span>
            <span class="chip">${escapeHtml(item.sourceLanguage)}</span>
          </div>
          <h3>${escapeHtml(item.title)}</h3>
          <p>${escapeHtml(item.summary)}</p>
          <p><strong>${escapeHtml(item.period)}</strong></p>
        </article>
      `;
    })
    .join("");

  renderReader();
}

function pickVoice(langCode) {
  const synth = window.speechSynthesis;
  const availableVoices = synth.getVoices();

  return availableVoices.find((voice) => voice.lang === langCode)
    || availableVoices.find((voice) => voice.lang.startsWith(langCode.split("-")[0]))
    || availableVoices.find((voice) => voice.lang.includes("en"))
    || null;
}

function stopSpeech() {
  if ("speechSynthesis" in window) {
    window.speechSynthesis.cancel();
  }
}

function speakSelectedText() {
  const item = library.find((entry) => entry.id === state.selectedId);
  const language = languages.find((entry) => entry.key === state.language);
  const status = document.getElementById("statusNote");

  if (!("speechSynthesis" in window)) {
    if (status) {
      status.textContent = "Speech synthesis is not available in this browser.";
    }
    return;
  }

  if (!item || !language) {
    return;
  }

  stopSpeech();

  const utterance = new SpeechSynthesisUtterance(item.texts[state.language]);
  utterance.lang = language.voice;
  utterance.rate = 0.9;
  utterance.pitch = 1;

  const chosenVoice = pickVoice(language.voice);
  if (chosenVoice) {
    utterance.voice = chosenVoice;
  }

  utterance.onstart = () => {
    if (status) {
      status.textContent = `Reciting ${item.title} in ${language.label}.`;
    }
  };

  utterance.onend = () => {
    if (status) {
      status.textContent = `Finished reciting ${item.title} in ${language.label}.`;
    }
  };

  utterance.onerror = () => {
    if (status) {
      status.textContent = `This browser could not play ${language.label} right now. Try another installed voice.`;
    }
  };

  window.speechSynthesis.speak(utterance);
}

function renderReader() {
  const item = library.find((entry) => entry.id === state.selectedId);

  if (!item) {
    readerPanel.className = "reader-panel is-empty";
    readerPanel.innerHTML = "<p>Select a work to open the multilingual reader.</p>";
    return;
  }

  readerPanel.className = "reader-panel";
  readerPanel.innerHTML = `
    <div class="reader-title">
      <div>
        <div class="reader-meta">
          <span class="pill">${escapeHtml(item.category)}</span>
          <span class="chip">${escapeHtml(item.sourceLanguage)}</span>
          <strong>${escapeHtml(item.period)}</strong>
        </div>
        <h3>${escapeHtml(item.title)}</h3>
      </div>
      <div class="reader-actions">
        <button class="button button-primary" type="button" id="playButton">Play Recitation</button>
        <button class="button button-secondary" type="button" id="stopButton">Stop</button>
      </div>
    </div>
    <p>${escapeHtml(item.summary)}</p>
    <div class="language-switcher">
      ${languages.map((language) => {
        const activeClass = language.key === state.language ? "is-active" : "";
        return `<button class="button language-button ${activeClass}" type="button" data-language="${language.key}">${language.label}</button>`;
      }).join("")}
    </div>
    <div class="text-sheet">
      <p>${escapeHtml(item.texts[state.language])}</p>
    </div>
    <div class="commentary-note">
      <span class="chip">Commentary</span>
      <p>${escapeHtml(item.commentary)}</p>
    </div>
    <div class="status-note" id="statusNote">Ready to recite this work in ${languages.find((entry) => entry.key === state.language)?.label}.</div>
  `;
}

function renderCommentaries() {
  commentaryGrid.innerHTML = commentaryHighlights
    .map((item) => `
      <article class="commentary-card">
        <span class="pill">${escapeHtml(item.tag)}</span>
        <h3>${escapeHtml(item.title)}</h3>
        <p>${escapeHtml(item.body)}</p>
      </article>
    `)
    .join("");
}

categoryFilters.addEventListener("click", (event) => {
  const button = event.target.closest("[data-category]");
  if (!button) {
    return;
  }

  state.category = button.dataset.category;
  renderFilters();
  renderLibrary();
});

libraryGrid.addEventListener("click", (event) => {
  const card = event.target.closest("[data-id]");
  if (!card) {
    return;
  }

  state.selectedId = card.dataset.id;
  renderLibrary();
});

readerPanel.addEventListener("click", (event) => {
  const languageButton = event.target.closest("[data-language]");

  if (languageButton) {
    state.language = languageButton.dataset.language;
    renderReader();
    return;
  }

  if (event.target.id === "playButton") {
    speakSelectedText();
  }

  if (event.target.id === "stopButton") {
    stopSpeech();
    const status = document.getElementById("statusNote");
    if (status) {
      status.textContent = "Recitation stopped.";
    }
  }
});

librarySearch.addEventListener("input", (event) => {
  state.query = event.target.value;
  renderLibrary();
});

if ("speechSynthesis" in window) {
  window.speechSynthesis.onvoiceschanged = () => {
    renderReader();
  };
}

renderFilters();
renderLibrary();
renderCommentaries();



